"""Export local Magma batch probes for BSD descent cases."""

from __future__ import annotations

import argparse
import json
from dataclasses import dataclass
from pathlib import Path


P3_OUT = "bsd_local_magma_p3_remaining.m"
P5_OUT = "bsd_local_magma_p5_probe.m"
README_OUT = "bsd_local_magma_readme.md"


@dataclass(frozen=True)
class DescentCase:
    label: str
    conductor: int
    a_invariants: list[int]
    prime: int
    target_primary_order: int
    expected_selmer_order: int
    predicted_sha_order: int
    predicted_sha_factorization: str
    torsion_primary_order: int | None = None

    @property
    def coeffs(self) -> str:
        return ", ".join(str(value) for value in self.a_invariants)


